/**
 * use to verify the sw2001 ic
 * @return: 
 * 		1 - verify pass ; 0 - verify failed
*/
int sw2001_verify(void)
{
	uint8_t tableNum;
	uint8_t result[16];
	uint8_t i;

	// 通过获取当前定时器的计数值作为查表随机数
	tableNum = get_ticks() % 10;	// 求余的值取决于 数据表 的个数
	printf("tableNum = %d\n", tableNum);

	//init I2C, used to acess sw2001
	iDeviceInit(SW2001_DEVICE_ADDR, 0);   //sw2001 default device address=0x3C, speed support 100k/400k

	//write cipher text to sw2001
	iWriteData(SW2001_REG_CIPHER_TEXT_ADDR, ct_table[tableNum], 16);

	//start sw2001 aes decrypt
	iSetBits(SW2001_REG_DECRYPT_CTRL, SW2001_AES_ENABLE);

	//wait for sw2001 decrypt complete
	iCheckBits(SW2001_REG_DECRYPT_CTRL, SW2001_AES_STATUS, 0x0);

	//read decrypt result back
	iReadData(SW2001_REG_PLAINT_TEXT_ADDR, result, 16);

    // close i2c device
	iDeviceDeInit();

	// printout the plain text
	printf("plaint = ");
	for (i=0; i<16; ++i) {
		printf("%x ", pt_table[tableNum][i]);
	}
	printf("\n");

	// printout the result text
	printf("result = ");
	for (i=0; i<16; ++i) {
		printf("%x ", result[i]);
	}
	printf("\n");
	
	//compare result
	for(i=0;i<16;i++) {
		if(pt_table[tableNum][i] != result[i]) {
			return 0;
		}
	}

	return 1;
}
